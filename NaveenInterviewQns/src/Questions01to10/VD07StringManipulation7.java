		//String length
		System.out.println(s.length());
		
		//Character present at particular index
		System.out.println(s.charAt(1));
		
		//Character present in Which index
		System.out.println(s.indexOf("s"));
	
		//Search char index at particular index
		System.out.println(s.indexOf("s", 9));
		
